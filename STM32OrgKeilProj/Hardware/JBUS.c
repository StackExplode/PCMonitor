void inline JBUS_DataRecCb(uint8_t* data,uint16_t len)
{
	static u8 SYB_FLAG = 0;
	for(u16 i=0;i<len;i++)
	{
		u8 curr = data[i];
		
		if(JBUSObj.WorkFlag == JB_RECING)
		{
			if(SYB_FLAG)
			{
				JBUSObj.WorkBuffer[JBUSObj.WorkPointer++] = curr;
				SYB_FLAG = 0;
			}
			else if(curr == 0x7D)
				SYB_FLAG = 1;
			else if(curr == 0x7E)
			{
				JBUSObj.WorkFlag = JB_REC_FINISH;
				
			}
			else
				JBUSObj.WorkBuffer[JBUSObj.WorkPointer++] = curr;
		}
		else if(JBUSObj.WorkFlag == JB_IDLE || JBUSObj.WorkFlag == JB_WAITREPLY)
		{
			if(curr == 0x7E)
				JBUSObj.WorkFlag = JB_RECING;
			else
			{
				JBUSObj.StartPointer++;
				continue;
			}
		}
		else
			continue;
	}
}



static u8 Check_Frame(void)
{
	
	if(JBUSObj.WorkPointer - JBUSObj.StartPointer < 9)
		return 5;
	JBUS_DATA_TYPE *data = (JBUS_DATA_TYPE*)&JBUSObj.WorkBuffer[JBUSObj.StartPointer];
#if !JBUS_REC_MISC	
	if(data->DestAddr != JBUS_ADDR)
	return 1;
#endif
	
#if !JBUS_REC_NOSUM
	u16 sum = Get_Sum((u8*)data,8);
	if(sum != SwapEndian16(data->Sum16))
		return 3;
#endif
//	if(SwapEndian16(data->Len) < LTU_LEN)
//		return 4;
	
#if !JBUS_REC_NOCRC
	u16 crc = Get_CRC16(data->Data,SwapEndian16(data->Len));
	if(crc != SwapEndian16(data->CRC16))
		return 2;
#endif	
	
	if(JBUSObj.Handlers[data->Fun] != 0)
	{
		JBUSObj.HandlerPointer = data->Fun;
		return 0;
	}
	else 
		return 255;
	
}

