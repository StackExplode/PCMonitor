uint8_t JBUS_Bind_Handler(uint8_t func,JBUS_HANDLE_FUN fun)
{
	if(JBUSObj.Handlers[func] == 0)
	{
		JBUSObj.Handlers[func] = fun;
		return 1;
	}
	return 0;
}

void JBUS_Delete_Handler(u8 func)
{
	JBUSObj.Handlers[func] = 0;
}





void JBUS_Execution(void)
{
	switch(JBUSObj.WorkFlag)
	{

		case JB_REC_FINISH:
		{
			u8 rt = Check_Frame();
			if(rt)
			{
				//Error Handle
				JBUSObj.WorkFlag = JB_IDLE;
			}
			else
				JBUSObj.WorkFlag = JB_HANDING;
			JBUSObj.WorkPointer = 0;
			break;
		}
		case JB_HANDING:(*JBUSObj.Handlers[JBUSObj.HandlerPointer])((JBUS_DATA_TYPE*)&JBUSObj.WorkBuffer[JBUSObj.StartPointer]);break;
		//case JB_REPLYING:case JB_CALLING:Send_Data();break;		
		
		default:break;
	}
}



