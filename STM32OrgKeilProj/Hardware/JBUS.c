__forceinline u16 SwapEndian16(void *data)
{
	u16 rt = 0;
	u8 * dt = (u8 *)data;
	((u8 *)(&rt))[1] = dt[0];
	((u8 *)(&rt))[0] = dt[1];
	return rt;
}

__forceinline u32 SwapEndian32(void* data)
{
	u32 rt = 0;
	u8 * dt = (u8 *)data;
	((u8 *)(&rt))[3] = dt[0];
	((u8 *)(&rt))[2] = dt[1];
	((u8 *)(&rt))[1] = dt[2];
	((u8 *)(&rt))[0] = dt[3];
	return rt;
}

void SwapEndian(u8 *data,u8 *dst,u8 len)
{
	u8 llen = len - 1;
	while(--len)
	{
		dst[len] = data[llen - len];
	}
}

