static void Fill_SendBuff(u8 addr,u8 fun,JBUS_FLAG_TYPE flag)
{
	u16 crc = Get_CRC16(JBUSObj.TXBufferPtr,JBUSObj.WorkPointer);
	u8 temp[11]={
		0x7E,
		JBUS_ADDR,
		addr,
		*((u8 *)&flag),
		((u8 *)&JBUSObj.WorkPointer)[1],
		((u8 *)&JBUSObj.WorkPointer)[0],
		fun,
		((u8 *)&crc)[1],
		((u8 *)&crc)[0],
	};
	u16 sum = Get_Sum(&temp[1],8);
	temp[9] = ((u8 *)&sum)[1];
	temp[10] = ((u8 *)&sum)[0];
	
	Append_TXBuffer(temp[0]);
	for(u8 i=1;i<11;i++)
	{
		if(temp[i] == 0x7E || temp[i] == 0x7D)
		{
			Append_TXBuffer(0x7D);
			Append_TXBuffer(temp[i]);
		}
		else
			Append_TXBuffer(temp[i]);
	}
	
	for(u16 i=0;i<JBUSObj.WorkPointer;i++)
	{
		if(JBUSObj.TXBufferPtr[i] == 0x7E || JBUSObj.TXBufferPtr[i] == 0x7D)
		{
			Append_TXBuffer(0x7D);
			Append_TXBuffer(JBUSObj.TXBufferPtr[i]);
		}
		else
			Append_TXBuffer(JBUSObj.TXBufferPtr[i]);
	}
	Append_TXBuffer(0x7E);
	
}

void JBUS_StartDataSend(u8 addr,u8 fun,JBUS_FLAG_TYPE flag)
{
	if(JBUSObj.WorkFlag == JB_IDLE)
		JBUSObj.WorkFlag = JB_CALLING;
	else if(JBUSObj.WorkFlag == JB_HANDING)
		JBUSObj.WorkFlag = JB_REPLYING;
	else
		return;
	Fill_SendBuff(addr,fun,flag);
	Send_Data();
}

void JBUS_FillBuffer(void* data,u16 len)
{
//	if(JBUSObj.WorkPointer + len > JBUS_MTU)
//		throw(0x11000001);
	memcpy(JBUSObj.TXBufferPtr+JBUSObj.WorkPointer,data,len);
	JBUSObj.WorkPointer += len;
}

void JBUS_FinishDataSend(void)
{
	if(JBUSObj.WorkFlag == JB_REPLYING)
	{
		JBUSObj.WorkFlag = JB_IDLE;
		JBUSObj.WorkPointer = 0;
	}
	else if(JBUSObj.WorkFlag == JB_CALLING)
	{
		JBUSObj.WorkFlag = JB_WAITREPLY;
		JBUSObj.WorkPointer = 0;
	}
}


